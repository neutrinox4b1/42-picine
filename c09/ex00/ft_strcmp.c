int	ft_strcmp(char *s1, char *s2)
{
	char	*sp1;
	char	*sp2;

	sp1 = s1;
	sp2 = s2;
	while (*sp1 != '\0' || *sp2 != '\0')
	{
		if (*sp1 != *sp2)
			return (*sp1 - *sp2);
		sp1++;
		sp2++;
	}
	return (0);
}
