char	*ft_strcat(char *dest, char *src)
{
	char	*dp;
	char	*sp;

	dp = dest;
	sp = src;
	while (*dp != '\0')
		dp++;
	while (*sp != '\0')
	{
		*dp = *sp;
		dp++;
		sp++;
	}
	*dp = '\0';
	return (dest);
}
