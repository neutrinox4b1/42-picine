char	*ft_strupcase(char *str)
{
	char		*sp;
	const char	up_offset = 'A' - 'a';

	sp = str;
	while (*sp != '\0')
	{
		if (*sp >= 'a' && *sp <= 'z')
		{
			*sp += up_offset;
		}
		sp++;
	}
	return (str);
}
