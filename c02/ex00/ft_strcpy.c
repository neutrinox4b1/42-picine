char	*ft_strcpy(char *dest, char *src)
{
	char	*dp;
	char	*sp;

	dp = dest;
	sp = src;
	while (*sp != '\0')
	{
		*dp = *sp;
		dp++;
		sp++;
	}
	*dp = '\0';
	return (dest);
}
