#include <unistd.h>

void	ft_print_comb(void)
{
	char	i;
	char	j;
	char	k;

	i = 0x30;
	while (i <= 0x37)
	{
		j = i + 1;
		while (j <= 0x38)
		{
			k = j + 1;
			while (k <= 0x39)
			{
				write(1, &i, 1);
				write(1, &j, 1);
				write(1, &k, 1);
				if (i != 0x37)
					write(1, ", ", 2);
				k++;
			}
			j++;
		}
		i++;
	}
}
