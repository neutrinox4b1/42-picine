#include <unistd.h>

void	ft_print(int *board)
{
	int		i;
	char	c;

	i = 0;
	while (i < 16)
	{
		c = board[i] + '0';
		write(1, &c, 1);
		if (i % 4 == 3)
		{
			write(1, "\n", 1);
		}
		else
		{
			write(1, " ", 1);
		}
		i++;
	}
	return ;
}
